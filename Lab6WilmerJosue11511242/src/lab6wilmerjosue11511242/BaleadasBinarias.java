    private void jButton5MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton5MouseClicked
        // TODO add your handling code here:
        if (jl_orden.getSelectedIndex() >= 0) {
            for (String t : baleadas) {
                lista.get(jl_orden.getSelectedIndex()).getLista().add(t);
            }
            ArrayList<String> baleadas = new ArrayList();
        } else {
            JOptionPane.showMessageDialog(jd_agregaror, "Seleccione una persona");
        }
        SimpleDateFormat hora = new SimpleDateFormat("hh");
        SimpleDateFormat fecha = new SimpleDateFormat("dd/MM/yy");
        listas.add(new Ordenes(numero, hora, fecha, jl_orden.getSelectedIndex()));
        numero += 7;
        JOptionPane.showMessageDialog(this, "Orden Finalizada");

    }//GEN-LAST:event_jButton5MouseClicked

