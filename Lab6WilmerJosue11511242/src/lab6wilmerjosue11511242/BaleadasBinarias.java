    private void jButton3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton3MouseClicked
        // TODO add your handling code here:
        if (tf_nombre.getText().length() > 0 && tf_telefono.getText().length() > 0 && tf_dinero.getText().length() > 0 && ta_direccion.getText().length() > 0) {
            lista.add(new Clientes(tf_nombre.getText(), Integer.parseInt(tf_telefono.getText()), Double.parseDouble(tf_dinero.getText()), ta_direccion.getText()));
            tf_nombre.setText("");
            tf_telefono.setText("");
            tf_dinero.setText("");
            ta_direccion.setText("");
            JOptionPane.showMessageDialog(this, "Cliente agregado");

        } else {
            JOptionPane.showMessageDialog(this, "Complete Todos los espacios");
            jd_cliente.setVisible(true);
        }
    }//GEN-LAST:event_jButton3MouseClicked

